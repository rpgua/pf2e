export const sortDate = (a: string, b:string, direction?: 'asc'|'desc') =>{
    if (direction==='asc') {
        return new Date(a).getTime() - new Date(b).getTime()
    } else {
        return new Date(b).getTime() - new Date(a).getTime()
    }
}

