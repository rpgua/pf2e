function getNonUniqueValues(arr:string[]) {
    const seen = new Set();
    const duplicates = new Set();
    arr.forEach(item => {
        if (seen.has(item)) {
            duplicates.add(item);
        } else {
            seen.add(item);
        }
    });
    return arr.filter(item => duplicates.has(item));
}

